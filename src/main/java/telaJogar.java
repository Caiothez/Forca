    public String Sorteia(){
        try {
            palavra = objCon.puxaPalavra();
            //JOptionPane.showMessageDialog(null, "ArerÃ©");
            return palavra;
        } catch (SQLException ex) {
            Logger.getLogger(telaJogar.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
    
